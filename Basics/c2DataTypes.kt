fun main(){
    val number = 1 // constant value and can never be changed
    println("Number : $number")

    var number1:Int = 1 // this variable value can be changed
    println("Before changing Number1 : $number1")
    number1 = 2
    println("After changing Number2 : $number1")

    val pi = 3.1415 // by default, it is double
    println("Pi : $pi")

    val cgpa = 3.9f // now it's float
    println("CGPA : $cgpa")

    val char = 'a'
    println("Char : $char")

    val name = "Muhammad Shahzaib"
    println("Name : $name")

    val bool = true
    println("Bool : $bool")
}