fun main(){
    // AND operator
    println("AND (true && false) : " + (true && false))
    println("AND (false && true) : " + (false && true))
    println("AND (true && true) : " + (true && true))
    println("AND (false && false) : " + (false && false))

    // OR operator
    println("OR (true && false) : " + (true || false))
    println("OR (false && true) : " + (false || true))
    println("OR (true && true) : " + (true || true))
    println("OR (false && false) : " + (false || false))

    // NOT operator
    println("NOT (true) : " + (!true))
    println("NOT (false) : " + (!false))
}